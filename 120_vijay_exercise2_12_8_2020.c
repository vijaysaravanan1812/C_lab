//program to add, subtract, multiply and divide 2 numbers

#include<stdio.h>

int main()
{
    int a1,a2;
    printf("Enter the first No\n");
    scanf("%d",&a1);
    printf("Enter the second No\n");
    scanf("%d",&a2);
    printf("Addition :  %d \n", a1 + a2 );
    printf("Subraction : %d \n", a1 - a2);
    printf("Multiplication : %d \n", a1 * a2);
    printf("Division : %d \n", a1 / a2);

    return 0;



}